  <h2>About</h2>

  <!-- <p style="color: var(--theme1);"> -->
  <p>
    Carbon offsetting has undermined real climate action, given rise to human
    rights and Indigenous rights violations, and caused severe harm to frontline
    communities for over two decades. Despite this, UN climate conferences have
    become the biggest promotional event for carbon offsets.<br />The fraud and
    harms to local communities caused by carbon market projects have been
    thoroughly documented. Since 2023, academic research, media and civil
    society investigations have exposed how these projects routinely generate
    phantom offsets and result in land grabbing and human and Indigenous rights
    violations. <sup style="font-size: 10px;"> 1</sup>
  </p>

  <p>
    The Carbon Violence archive exposes the violent incidents <sup
      style="font-size: 10px;"
    >
      2</sup
    > that occur to individuals and communities living in areas where carbon offsetting
    projects are implemented, making accountable project developers, subsidiaries,
    investors, funders, validation and verification bodies, brokers, corporations
    purchasing credits.
  </p>

  <p>
    The idea emerged following the publication of the Offset Archive, <sup
      style="font-size: 10px;"
    >
      3</sup
    >
    a searchable database of every project listed in the four major carbon
    registories:
    <a href="https://acrcarbon.org/">American Carbon Registry (ACR)</a>,
    <a href="https://www.climateactionreserve.org/"
      >Climate Action Reserve (CAR)</a
    >, <a href="https://www.goldstandard.org/">Gold Standard</a>, and
    <a href="https://verra.org/">Verra.</a>
    <sup style="font-size: 10px;"> 4</sup><br />These platforms make up the
    majority of the voluntary carbon market and record lists of various carbon
    offsetting projects, as well as the different methodologies that underlie
    them. The voluntary market is one of two distinct domains for carbon
    offsetting today and is open to the public to develop or purchase carbon
    offsetting products. The other domain is the mandatory or “compliance”
    marketplaces that form part of regulatory schemes run by governments and
    which are the basis for reaching international climate commitments. Despite
    high hopes at the most recent COP meeting in 2023 for regulation of
    voluntary carbon market, an agreement on key rules for carbon trading was
    not reached. Voluntary markets remain unregulated by the UN or governmental
    bodies.
  </p>

